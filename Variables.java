package First;
//Primitive Variables
public class Variables {
	public static void main(String args[])
	{
	// integer datatype
	int a = 5;
	int b = 6;
	System.out.println(a+b);
	
    //float datatype
	float c = 5.0f;
	float d = 6.0f;
	System.out.println(c+d);
	
	//character datatype
	char e = 'A';
	System.out.println(e);
	
	//Type Casting
	System.out.println((int)e);
	System.out.println((char)a);
	System.out.println((char)b);
}
}
